/*
Codewars Kata Details:

    - Title: Opposite number
    - Difficulty: 8 kyu
    - Link: https://www.codewars.com/kata/opposite-number
    - Language: Javascript

Description: 

    Very simple, given a number, find its opposite.

Examples:

    1: -1
    14: -14
    -34: 34
*/

// Solution

const opposite = number => number * -1

opposite(1) // -1

